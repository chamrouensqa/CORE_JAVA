public class while_loop {
    public static void main(String[] args) {

        //Increasing number from 1 to 10
        int num_increase = 1;
        while (num_increase <= 10){
            System.out.println("This increasing of the number is "+num_increase);
            num_increase++;
        }

        //Decreasing number from 10 to 0
        int number_decrease = 10;
        while (number_decrease >= 0){
            System.out.println("This decreasing number is "+number_decrease);
            number_decrease--;
        }
    }
}
